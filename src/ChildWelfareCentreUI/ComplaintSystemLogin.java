    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        	  String username = txtUsername.getText();
        String password = txtPassword.getText();
        String role = "";
        String checkPassword = "";
        
        if (username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all the Fields", "Try Again",JOptionPane.ERROR_MESSAGE);
        }else{
            try{
            ResultSet resultSet = null;
            resultSet = DatabaseConnection.getPersonCWCRole(username, false);
            
            while (resultSet.next()) {
                checkPassword = resultSet.getString(1);
		role = resultSet.getString(2);
            }
            if (password.equals(checkPassword)){
                
                switch(role) {
                    case "Complaint Handler":
                        ComplainHandlerHome CHH = new ComplainHandlerHome(username);
                            CHH.show();
                            dispose();
                      break;
                    case "user":
                        ComplainantHome CH = new ComplainantHome(username);
                            CH.show();
                            dispose();
                      break;
                }
            }else{
            
                JOptionPane.showMessageDialog(this, "Wrong Username or Password", "Try Again",JOptionPane.ERROR_MESSAGE);
                txtUsername.setText("");
                txtPassword.setText("");
                
            }
            }catch(Exception e){
                System.out.println("Error while Connecting");
                e.printStackTrace();
            } 
        }
    }//GEN-LAST:event_btnLoginActionPerformed

